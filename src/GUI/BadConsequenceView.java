    public void setBadConsequence(BadConsequence bc){
        this.badConsequenceModel=bc;
        this.name.setText("Esto es un mal rollo: "+badConsequenceModel.getText());
        this.levels.setText("Niveles:"+Integer.toString(badConsequenceModel.getLevels()));
        if(badConsequenceModel instanceof NumericBadConsequence){
            this.hiddenTreasures.setText("Ocultos:"+
                Integer.toString(((NumericBadConsequence)badConsequenceModel).getNHiddenTreasures()) );
            this.visibleTreasures.setText("Visibles:"+
                Integer.toString(((NumericBadConsequence)badConsequenceModel).getNVisibleTreasures()) );
        }else{
            if(badConsequenceModel instanceof SpecificBadConsequence){
                this.hiddenTreasures.setText("Ocultos:"+
                    ((SpecificBadConsequence)badConsequenceModel).getSpecificHiddenTreasures() );
                this.visibleTreasures.setText("Visibles:"+
                        ((SpecificBadConsequence)badConsequenceModel).getSpecificVisibleTreasures() );
            }else{
                this.hiddenTreasures.setText("Ocultos:10");
                this.visibleTreasures.setText("Visibles:10"); 
            }
        }
        this.repaint();
    }
